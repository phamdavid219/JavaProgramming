package day17_While_DoWhile;

public class DoWhileLoop {
    public static void main(String[] args) {
        boolean a = true;
        while(a){
            System.out.println("Wooden Spoon");
        }
        System.out.println("-----------------");
        do{
            System.out.println("Wooden Spoon");
        }while(a);
    }
}
